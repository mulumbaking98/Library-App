public class Event {
	private boolean startSession;
	private boolean endSession;
	/**
	 * @return the startSession
	 */
	public boolean isStartSession() {
		return startSession;
	}
	/**
	 * @param startSession the startSession to set
	 */
	public void setStartSession(boolean startSession) {
		this.startSession = startSession;
	}
	/**
	 * @return the endSession
	 */
	public boolean isEndSession() {
		return endSession;
	}
	/**
	 * @param endSession the endSession to set
	 */
	public void setEndSession(boolean endSession) {
		this.endSession = endSession;
	}


}//end event